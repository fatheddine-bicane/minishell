#include "../parser.h"

char	*sn_substr(char const *s, unsigned int start, size_t len)
{
	char	*subst;
	size_t	size;

	if (!s)
		return (NULL);
	if (start >= sn_strlen(s))
		return (sn_strdup(""));
	size = sn_strlen(s + start);
	if (size < len)
		len = size;
	subst = (char *)malloc(sizeof(char) * (len + 1));
	if (!subst)
		return (NULL);
	sn_strlcpy(subst, s + start, len + 1);
	return (subst);
}
